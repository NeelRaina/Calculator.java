
public class Calculator {

	public Calculator(){
			
	}
	
	public int addNum(int x, int y) 
	{
		return x + y;
	}
	
	public int subNum(int x, int y)
	{
		return x - y;
	}
	
	public int multNum(int x, int y)
	{
		return x * y;	
	}
	
	public int divideNum(int x, int y)
	{
		return x/y;
	}
	
	public int remainderNum(int x,int y) 
	{
		return x % y;
	}
}
